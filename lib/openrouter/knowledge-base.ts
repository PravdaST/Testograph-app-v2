/**
 * Build comprehensive knowledge base prompt for system
 */
export function buildKnowledgeBasePrompt(): string {
  let prompt = `

БАЗА ДАННИ ОТ НАУЧЕН ЦЕНТЪР (testograph.eu/learn):
Използвай тази информация за да даваш КОНКРЕТНИ, ДЕТАЙЛНИ отговори.
Когато отговаряш, цитирай факти от статиите и ВИНАГИ препоръчвай релевантна статия с линк.

`

  for (const article of ARTICLES_CONTENT) {
    prompt += `
═══ ${article.title} ═══
Категория: ${article.category}
Линк: ${article.url}

TL;DR: ${article.tldr}

Ключови факти:
${article.keyFacts.map(f => `• ${f}`).join('\n')}
`

    if (article.practicalTips && article.practicalTips.length > 0) {
      prompt += `
Практични съвети:
${article.practicalTips.map(t => `→ ${t}`).join('\n')}
`
    }

    if (article.faq && article.faq.length > 0) {
      prompt += `
FAQ:
${article.faq.map(f => `Q: ${f.q}\nA: ${f.a}`).join('\n\n')}
`
    }

    prompt += '\n'
  }

  prompt += `
═══════════════════════════════════════════════
ВАЖНИ ПРАВИЛА ЗА ОТГОВОРИ:
1. Давай КОНКРЕТНИ съвети базирани на информацията по-горе
2. Цитирай ключови факти когато са релевантни
3. ВИНАГИ препоръчвай статия с формат: [[ARTICLE:заглавие|линк]] - например [[ARTICLE:Сън и тестостерон|https://testograph.eu/learn/lifestyle/san-i-testosteron-vliyanie-na-sunia]]
4. Ако въпросът е за TestoUp - използвай информацията за съставките и действието
5. Ако въпросът е за сън/хранене/тренировки - дай практични съвети от статиите
6. НИКОГА не измисляй линкове - използвай САМО линковете от статиите по-горе!
═══════════════════════════════════════════════
`

  return prompt
}

// Export for backward compatibility
export interface Article {
  title: string
  url: string
  category: string
  description: string
  keywords: string[]
}

export const ARTICLES: Article[] = ARTICLES_CONTENT.map(a => ({
  title: a.title,
  url: a.url,
  category: a.category,
  description: a.tldr,
  keywords: a.keyFacts.slice(0, 3).map(f => f.split(' ').slice(0, 3).join(' ')),
}))
